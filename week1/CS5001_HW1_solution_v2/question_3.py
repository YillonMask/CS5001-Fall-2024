"""
3. Printing the Type of Variables

Write a Python program that assigns a string, an integer, and a floating-point number to three different variables. 
Then, use the type() function to print out the type of each variable.

Example Output:
The type of variable ’name’ is: <class ’str’>
The type of variable ’age’ is: <class ’int’>
The type of variable ’height’ is: <class ’float’>

Hint: Use the type() function to display the data type of each variable.
"""

# Assign a string, an integer, and a floating-point number to three different variables
name = "Alice"
age = 23
height = 5.7

# Display the type of each variable
print(f"The type of variable 'name' is: {type(name)}")
print(f"The type of variable 'age' is: {type(age)}")
print(f"The type of variable 'height' is: {type(height)}")
